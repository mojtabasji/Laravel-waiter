    public function pay(Facture $facture)
    {
        $facture->payed = 1;
        $facture->save();

        $table = $facture->user;
        $table->status = 0;
        $table->member_id = 0;
        $table->logged = 0;
        $table->save();
        return redirect()->back();
    }

